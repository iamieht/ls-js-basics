// Find out how we can join two or more strings together.

//  The operator + concatenates strings together and the String.prototype.concat() method.

let firstName = "Ada";
let lastName = "Lovelace";

"".concat(firstName, " ", lastName);
// 'Ada Lovelace'

let firstName2 = "Ada";
let lastName2 = "Lovelace";

firstName2 + " " + lastName2;
// 'Ada Lovelace'
